# Aspas simples
print('Allan Richard')
#Aspas duplas
print("Allan Richard")
#Escape
print("Allan \"Richard\"") #Caractere de escape ler mas não enterpreta a string
#r 
print(r"Allan \"Richard\"") #Posso usar o r para mostrar o caractere de escape
#Apas duplas dentro de aspas simples
print(1, 'Allan "Richard"') #Truque para não precisar usa o r e nem o escape para não complicar meu código
#aspas simples dentro de aspas duplas
print(2, "Allan 'Richard")