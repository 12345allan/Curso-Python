# elif e else depende do if
# O if não depende de ninguem 
# if  / elif      / else
# se / se não se / se não
entrada = input('Voce quer "entrar" ou "sair"? ')

if entrada == 'entrar': 
    print('Voce entrou no sistema')
elif entrada == 'sair':
    print('Voce saiu do sistema')
else:
    print('Voce não digitou nem entrar e nem sair')
