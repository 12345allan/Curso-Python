# input coleta dados de úsuarios ou informções e o input sempre retornará uma string na saida do programa 


numero_1 = input('Digite um número: ')
numero_2 = input('Digite outro número: ')

# Criando duas novas variaveis para fazer a conversão de uma string para um inteiro  
int_numero_1 = int(numero_1)
int_numero_2 = int(numero_2)

print(f'A soma dos números é: {int_numero_1 + int_numero_2}')