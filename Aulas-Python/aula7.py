# Operadores Aritmetricos

adicao = 10 + 10 
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 2.2 # Aqui sempre retornará um número de ponto flutuante (Float)
print('Divisão', divisao)

divisao_inteira = 10 // 2.2 # Ela corta as casas decimais
print('Divisão Inteira', divisao_inteira)

exponenciacao = 2 ** 10
print('Exponeciação', exponenciacao)

modulo = 25 % 5 # Resto da divisão 
print('Modulo', modulo)
