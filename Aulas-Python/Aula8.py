# Concatenação (+) e repetição (*) com operadores aritmetricos
# concatenação é juntar os valores usando o sinal de + e usar strings para fazer a cocatenação 
contatenacao = 'A' + 'B' + 'C'
print(contatenacao)

# Repetição usando o operador de multiplicação com um número inteiro e uma string qualquer
a_dez_vezes = 'A' * 10
tres_vezes_luiz = 3 * 'luiz'
print(a_dez_vezes)
print(tres_vezes_luiz)